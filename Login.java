    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        String companyId=jTextField8.getText();
        String gstNo=jTextField7.getText();
        char pass1[]=jPasswordField4.getPassword();
        String nPass=new String(pass1);
        char cpass1[]=jPasswordField5.getPassword();
        String cnPass=new String(cpass1);
        SessionFactory sf=ConnectionFactory.emergencyConnection();
        Session session=sf.openSession();
        Transaction tx=session.beginTransaction();
        Criteria ct=session.createCriteria(Company.class);
        ct.add(Restrictions.and(Restrictions.eq("companyId",companyId),Restrictions.eq("gstNo", gstNo)));
        List<Company> list=ct.list();
        if(list.isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Invalid Company ID/GST Number");
        }
        else
        {
            if(nPass.equals(cnPass))
            {
                Company con=(Company)session.get(Company.class, gstNo);
                con.setPassword(nPass);
                session.update(con);
                tx.commit();
                JOptionPane.showMessageDialog(this, "Password Changed");
            }
            else
            {
                JOptionPane.showMessageDialog(this, "New Password and Confirm New Passsword does not match");
            }
        }
    }//GEN-LAST:event_jButton7ActionPerformed

