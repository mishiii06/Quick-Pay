    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        companyId=jTextField4.getText();
        String compantName=jTextField5.getText();
        String registrationId=jTextField2.getText();
        String contactNo=jTextField1.getText();
        String address=jTextArea1.getText();
        String gstNo=jTextField3.getText();
        String billTypeElec="", billTypeShop="", billTypeGas="", billTypeTele="";
        if(jRadioButton4.isSelected()==true)
        {
            billTypeElec=jRadioButton4.getActionCommand()+",";
        }
        if(jRadioButton1.isSelected()==true)
        {
            billTypeShop=jRadioButton1.getActionCommand()+",";
        }
        if(jRadioButton2.isSelected()==true)
        {
            billTypeGas=jRadioButton2.getActionCommand()+",";
        }
        if(jRadioButton3.isSelected()==true)
        {
            billTypeTele=jRadioButton3.getActionCommand()+",";
        }
        String billType=billTypeElec+billTypeShop+billTypeGas+billTypeTele;
        int length=billType.length();
        char ch[]=new char[length];
        char ch1[]=new char[length-1];
        for(int i=0;i<length;i++)
        {
            ch[i]=billType.charAt(i);
        }
        for(int j=0;j<length-1;j++)
        {
            ch1[j]=ch[j];
        }
        String billType1=new String(ch1);
        char pass1[]=jPasswordField2.getPassword();
        String npass=new String(pass1);
        char cpass1[]=jPasswordField1.getPassword();
        String cnpass=new String(cpass1);
        
        if(npass.equals(cnpass))
        {
            SessionFactory sf=ConnectionFactory.emergencyConnection();
            Session session=sf.openSession();
            Transaction tx=session.beginTransaction();
            Company log=new Company(companyId, compantName, registrationId, contactNo, address, gstNo, billType1, npass);
            session.save(log);
            tx.commit();
            cl.show(jPanel1, "login");
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Password and Confirm Password does not match");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

